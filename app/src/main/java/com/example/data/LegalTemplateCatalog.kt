package com.example.data

private data class TemplateSeed(
    val title: String,
    val category: String,
    val caseType: String,
    val description: String,
    val fields: List<String>,
    val body: String
)

private fun TemplateSeed.toEntity(): LegalTemplate {
    return LegalTemplate(
        title = title,
        category = category,
        caseType = caseType,
        description = description,
        requiredFieldsJson = fields.joinToString(prefix = "[", postfix = "]") { "\"$it\"" },
        templateBody = body.trimIndent().trim()
    )
}

