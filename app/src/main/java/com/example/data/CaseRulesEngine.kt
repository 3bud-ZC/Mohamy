    fun resolveCaseType(raw: String, normalize: (String) -> String): String {
        val n = normalize(raw)
        return when {
            n.contains("ايجار") -> "إيجارات"
            n.contains("اسره") -> "أسرة"
            n.contains("جنائي") -> "جنائي"
            n.contains("مدني") -> "مدني"
            n.contains("تجاري") -> "تجاري"
            n.contains("عمال") -> "عمال"
            n.contains("ميراث") -> "ميراث"
            n.contains("عقار") -> "عقارات"
            else -> "عام"
        }
    }

    fun getRules(caseType: String, normalize: (String) -> String): CaseTypeRules {
        val key = resolveCaseType(caseType, normalize)
        return rules[key] ?: rules.getValue("عام")
    }
